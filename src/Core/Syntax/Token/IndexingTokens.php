<?php
namespace Novaxis\Core\Syntax\Token;

/**
 * Trait IndexingTokens
 *
 * This trait contains a set of constants related to indexing tokens.
 */
trait IndexingTokens {
	/**
	 * Represents the opening square bracket used for indexing.
		*/
	const OPEN_INDEX = '[';
	
	/**
	 * Represents the closing square bracket used for indexing.
		*/
	const CLOSE_INDEX = ']';
}